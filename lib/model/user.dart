class User {
  final String id;
  final String name;
  final num age;
  final String email;
  final String bio;
  final num phoneNo;
  final String jobProfile;
  final String profilePic;
  final String address;

  const User({
    required this.id,
    required this.name,
    required this.age,
    required this.email,
    required this.bio,
    required this.phoneNo,
    required this.jobProfile,
    required this.profilePic,
    required this.address,
  });
}
